#!/usr/bin/env bash
set -euo pipefail

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/../.." && pwd)"
PROVISIONER_SKETCH="$ROOT_DIR/arduino-code/esp32-provisioner/ESP32_Provisioner/ESP32_Provisioner.ino"
PROVISIONER_DIR="$(dirname "$PROVISIONER_SKETCH")"
CONFIG_H="$PROVISIONER_DIR/provisioning_config.h"
DEFAULT_PORT="/dev/ttyUSB0"
FQBN="esp32:esp32:esp32:PartitionScheme=huge_app,PSRAM=enabled"

resolve_arduino_cli() {
  if [[ -x "${HOME}/mymachine/bin/arduino-cli" ]]; then
    printf '%s\n' "${HOME}/mymachine/bin/arduino-cli"
    return
  fi
  if command -v arduino-cli >/dev/null 2>&1; then
    command -v arduino-cli
    return
  fi
  printf 'Could not find arduino-cli in ~/mymachine/bin or PATH.\n' >&2
  exit 1
}

usage() {
  cat <<'EOF'
Usage: provision-car-wrover.sh --ssid SSID --password PASSWORD --hostname HOSTNAME --car-name NAME [port]

Provisions an ESP32-WROVER car with Wi-Fi credentials, mDNS hostname, and car
identity. Flashes a provisioner sketch that writes to NVS, then flashes the
main WS firmware. NVS data persists across future firmware flashes.

Required:
  --ssid       Wi-Fi network name
  --password   Wi-Fi password (use '' for open networks)
  --hostname   mDNS hostname, e.g. hiro1  (car reachable as hiro1.local on LAN)
  --car-name   Human-readable car name, e.g. "Hiro 1"

Optional:
  port         Serial port. Default: /dev/ttyUSB0

Examples:
  arduino-code/bin/provision-car-wrover.sh --ssid ClassNet --password secret --hostname hiro1 --car-name "Hiro 1"
  arduino-code/bin/provision-car-wrover.sh --ssid ClassNet --password secret --hostname hiro2 --car-name "Hiro 2" /dev/ttyUSB1
EOF
}

SSID=""
PASSWORD=""
HOSTNAME=""
CAR_NAME=""
PORT=""

while [[ $# -gt 0 ]]; do
  case "$1" in
    --ssid)      SSID="$2";      shift 2 ;;
    --password)  PASSWORD="$2";  shift 2 ;;
    --hostname)  HOSTNAME="$2";  shift 2 ;;
    --car-name)  CAR_NAME="$2";  shift 2 ;;
    -h|--help)   usage; exit 0 ;;
    *)           PORT="$1";      shift ;;
  esac
done

if [[ -z "$SSID" || -z "$HOSTNAME" || -z "$CAR_NAME" ]]; then
  printf 'Error: --ssid, --hostname, and --car-name are required.\n' >&2
  usage
  exit 1
fi

PORT="${PORT:-$DEFAULT_PORT}"
ARDUINO_CLI="$(resolve_arduino_cli)"

# Escape values for C string literals
esc() { printf '%s' "$1" | sed "s/\\\\/\\\\\\\\/g; s/\"/\\\\\"/g"; }

printf '=== Generating provisioning_config.h ===\n'
cat > "$CONFIG_H" <<EOF
// Generated by provision-car-wrover.sh — do not edit manually.
#ifndef PROVISIONING_CONFIG_H
#define PROVISIONING_CONFIG_H
#define PROV_SSID      "$(esc "$SSID")"
#define PROV_PASSWORD  "$(esc "$PASSWORD")"
#define PROV_HOSTNAME  "$(esc "$HOSTNAME")"
#define PROV_CAR_NAME  "$(esc "$CAR_NAME")"
#endif
EOF
printf 'SSID:     %s\n' "$SSID"
printf 'Hostname: %s\n' "$HOSTNAME"
printf 'Car name: %s\n' "$CAR_NAME"
printf 'Port:     %s\n' "$PORT"
printf 'FQBN:     %s\n' "$FQBN"

printf '\n=== Step 1/2: Flash provisioner (writes NVS) ===\n'
"$ARDUINO_CLI" compile --fqbn "$FQBN" "$PROVISIONER_SKETCH"
"$ARDUINO_CLI" upload -p "$PORT" --fqbn "$FQBN" "$PROVISIONER_SKETCH"
printf 'Provisioner flashed. Waiting for NVS write...\n'
sleep 3

printf '\n=== Step 2/2: Flash main WS firmware ===\n'
MAIN_SKETCH="$ROOT_DIR/arduino-code/esp32-camera/ESP32_CameraServer_WS/ESP32_CameraServer_WS.ino"
"$ARDUINO_CLI" compile --fqbn "$FQBN" "$MAIN_SKETCH"
"$ARDUINO_CLI" upload -p "$PORT" --fqbn "$FQBN" "$MAIN_SKETCH"

printf '\n=== Provisioning complete ===\n'
printf 'Car "%s" should boot as %s.local on your network.\n' "$CAR_NAME" "$HOSTNAME"
printf 'Monitor boot output: stty -F %s 115200 raw -echo && cat %s\n' "$PORT" "$PORT"
